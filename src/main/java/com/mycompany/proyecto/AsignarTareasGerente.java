    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        boolean ingresado = false;
            
            for(Tarea t : Proyecto.tareas){
                String usuarioSeleccionado = miembroComboBox.getSelectedItem().toString();
                String usuarioTarea = t.getUsuario();
    
                if (usuarioSeleccionado != null && usuarioTarea != null && usuarioTarea.equals(usuarioSeleccionado)) {
                ingresado = true;
                break;
    }
            }
            if(ingresado){
                JOptionPane.showMessageDialog(this, "Tarea ya se encuentra asignada a otro usuario, por favor seleccione otro");
            }
            else{
                 
                Tarea tareaSeleccionada = null;
                for(Tarea t: Proyecto.tareas){
                    if(t.getTituloTarea().equals(tareaComboBox.getSelectedItem().toString())){
                        tareaSeleccionada = t;
                        break;
                    }
                }
                String usuario = miembroComboBox.getSelectedItem().toString();
                
                if(usuario!=null && tareaSeleccionada!=null){
                    tareaSeleccionada.setUsuario(usuario);
                    JOptionPane.showMessageDialog(this, "Tarea asignada exitosamente");
                    tareaComboBox.removeItem(tareaSeleccionada);
                }
                
            }
    }//GEN-LAST:event_jButton1ActionPerformed



