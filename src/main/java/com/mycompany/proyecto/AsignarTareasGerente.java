    public void llenarComboBoxMi(){
        
        Equipo equipoSeleccionado = null;
        for(Equipo e : Proyecto.equipos){
            if(e.getGerente().equals(PaginaGerente.usuario.getNombre())){
                equipoSeleccionado = e;
                for(Usuario u: equipoSeleccionado.getMiembros()){
                    miembroComboBox.addItem(u.getUsuario());
                }
                break;
            }
        }
          
    }
    
    public void llenarComboBoxTarea(){
     
        Proyectos proyectoSeleccionado = null;
        for(Proyectos p : Proyecto.proyectos){
            if(p.getNombreEquipo().equals(PaginaGerente.usuario.getNombreEquipo())){
                proyectoSeleccionado = p;
                for(Tarea t: proyectoSeleccionado.getTareas()){
                    tareaComboBox.addItem(t.getTituloTarea());
                }
                break;
            }
        }
    }

