    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        modificar = null;
        for (Tarea t: Proyecto.tareas){
           
            if(t.getTituloTarea().equals(tituloTextField.getText())){
                modificar = t;
                break;
            }
        }
        if(modificar != null){
            nombreTextField.setText(modificar.getTituloTarea());
            descripcionTextField.setText(modificar.getDescripcionTarea());
            finicioTextField.setText(modificar.getFechaInicio());
            ffinalizacionTextField.setText(modificar.getFechaFinalizacion());

        }
        else{
            JOptionPane.showMessageDialog(this, "Tarea no encontrado");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(modificar != null){
            modificar.setTituloTarea(nombreTextField.getText());
            modificar.setDescripcionTarea(descripcionTextField.getText());
            modificar.setFechaInicio(finicioTextField.getText());
            modificar.setFechaFinalizacion(ffinalizacionTextField.getText());
            modificar.setEstado(estadoComboBox.getSelectedItem().toString());
            JOptionPane.showMessageDialog(this,"Tarea modificadada exitosamente");
            
            tituloTextField.setText("");
            nombreTextField.setText("");
            descripcionTextField.setText("");
            finicioTextField.setText("");
            ffinalizacionTextField.setText("");
        }
        else{
            JOptionPane.showMessageDialog(this, " ");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    

