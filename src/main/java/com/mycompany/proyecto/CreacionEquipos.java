    private void crearButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearButtonActionPerformed
        // TODO add your handling code here:

        boolean ingresado = false;

        for (Equipo e: Proyecto.equipos){
            if (e.getNombreEquipo().equals(nombreEquipoTextField.getText())){
                ingresado = true;
                break;
            }
        }
        if(ingresado){
            JOptionPane.showMessageDialog(this, "Nombre de Equipo en uso, por favor elija otro");
        }

        else{
            boolean ingresado3 = false;
            for(Equipo e: Proyecto.equipos){
                if (e.getGerente().equals(gerenteComboBox.getSelectedItem())){
                    ingresado3 = true;
                    break;
                }
            }
            if(ingresado3){
                JOptionPane.showMessageDialog(this, "Gerente ya se encuentra asignado a otro equipo, por favor seleccione otro");
            }
            else{

                String nombreEquipo = nombreEquipoTextField.getText();
                String gerente = gerenteComboBox.getSelectedItem().toString();

                Equipo equipoNuevo = new Equipo();
                equipoNuevo.setNombreEquipo(nombreEquipo);
                equipoNuevo.setId(id);
                equipoNuevo.setGerente(gerente);

                Proyecto.equipos.add(equipoNuevo);
                
                    Equipo equipoSeleccionado = equipoNuevo;

                    Usuario miembroSeleccionado = null;
                    for(Usuario u: Proyecto.usuarios){
                        if(u.getNombre().equals(gerenteComboBox.getSelectedItem().toString())){
                            miembroSeleccionado = u;
                            break;
                        }
                    }

                    if(equipoSeleccionado!=null && miembroSeleccionado!=null){
                        equipoSeleccionado.asignarMiembro(miembroSeleccionado);
                        nombreEquipoTextField.setText("");
                        idEquipoTextField.setText("");
                        gerenteComboBox.removeItem(gerente);
                        JOptionPane.showMessageDialog(this, "Equipo creado con exitosamente");
                    }
            }
        }
    }//GEN-LAST:event_crearButtonActionPerformed

    
    
