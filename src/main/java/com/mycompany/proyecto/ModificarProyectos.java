    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        modificar = null;
        for (Proyectos p: Proyecto.proyectos){
           
            if(p.getNombreProyecto().equals(nombremTextField.getText())){
                modificar = p;
                break;
            }
        }
        if(modificar != null){
            nombreTextField.setText(modificar.getNombreProyecto());
            finicioTextField.setText(modificar.getFechaInicio());
            ffinalizacionTextField.setText(modificar.getFechaFinalizacion());

        }
        else{
            JOptionPane.showMessageDialog(this, "Proyecto no encontrado");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void modificarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarButtonActionPerformed
        // TODO add your handling code here:
        if(modificar != null){
            modificar.setNombreProyecto(nombreTextField.getText());
            modificar.setFechaInicio(finicioTextField.getText());
            modificar.setFechaFinalizacion(ffinalizacionTextField.getText());
            JOptionPane.showMessageDialog(this,"Proyecto modificado");
            
            nombremTextField.setText("");
            nombreTextField.setText("");
            finicioTextField.setText("");
            ffinalizacionTextField.setText("");
        }
        else{
            JOptionPane.showMessageDialog(this, " ");
        }
    }//GEN-LAST:event_modificarButtonActionPerformed



