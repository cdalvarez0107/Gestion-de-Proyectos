    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        JFileChooser jfc = new JFileChooser();
        FileNameExtensionFilter fef = new FileNameExtensionFilter("Seleccionar archivo CSV", "csv");
        jfc.setFileFilter(fef);
        
        int seleccion = jfc.showOpenDialog(this);
        
        if(seleccion == JFileChooser.APPROVE_OPTION){
            File f = jfc.getSelectedFile();
            String texto = leerArchivoTexto(f.getPath());
            jTextArea1.setText(texto);
            JOptionPane.showMessageDialog(this, "Usuarios cargados con exito");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    public String leerArchivoTexto(String ruta){
    
        String contenido = "";
        try{
         File f  = new File(ruta);
         FileReader fr = new FileReader(f);
         BufferedReader br = new BufferedReader(fr);
         String linea;
         while((linea = br.readLine())!=null){
             contenido+=linea+"\n";
             Usuario usuarioNuevo = new Usuario();
             String[] campos = linea.split("\\|");
             usuarioNuevo.setCorreo(campos[0]);
             usuarioNuevo.setNombre(campos[1]);
             usuarioNuevo.setApellido(campos[2]);
             usuarioNuevo.setPassword(campos[3]);
             usuarioNuevo.setRol(campos[4]);
             usuarioNuevo.setUsuario(campos[5]);
             Proyecto.usuarios.add(usuarioNuevo);
             
        }
        }catch(Exception e){
            System.out.println("Error al leer el archivo: "+e.toString());
        }
              
        return contenido;
    }
    

