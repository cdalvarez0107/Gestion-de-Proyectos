    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int i = -1;
        
        for(int j=0; j<Proyecto.usuarios.size();j++){
            if(Proyecto.usuarios.get(j).getUsuario().equals(usuarioTextField.getText())){
                i = j;
                break;
            }
        }
        if(i>=0){
            Proyecto.usuarios.remove(i);
            JOptionPane.showMessageDialog(this,"Usuario eliminado exitosamente");
            usuarioTextField.setText("");
        }
        else{
            JOptionPane.showMessageDialog(this,"Usuario no encontrado, por favor revise nuevamente el usuario ingresado");
        }   

    }//GEN-LAST:event_jButton1ActionPerformed


